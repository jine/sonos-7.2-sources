/*****************************************************************************
 *  Format
 *****************************************************************************/
int ov7673hw_set_format( u32 captureSizeFormat, u32 colorFormat, u32 mode)
{

	int	status;
	u8	*regsP, regValue;
	const u8	*defaultDataP;
	struct OV7673_SETTING  *ov7673_setting;

	defaultDataP  = NULL;
	for(ov7673_setting = ov7673_setting_table;
		ov7673_setting->format != 0;ov7673_setting++) {
		if ((colorFormat == ov7673_setting->format) &&
			(captureSizeFormat == ov7673_setting->resolution)) {
			defaultDataP = ov7673_setting->setting;
			break;
		}
	}

	if (defaultDataP == NULL)
		return -EPERM;

	/* reset */
	regValue = 0x80;
	write_sensor_reg(OV7673_COM7, &regValue);

	/* Get the pointer to the basic setting.
	 * The pointer must be freed after exiting.
	 */
	regsP = (u8 *) defaultDataP;

	/* Blast the entire parameter tree into the part */
	status = ov7673hw_set_regs( regsP );

	return 0;
}

